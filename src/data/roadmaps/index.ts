export function getRoadmapByBranch(branch: string): SemesterPlan[] {
  return ROADMAP_MAP[branch as keyof typeof ROADMAP_MAP] ?? [];
}

export function getCurrentSemesterPlan(branch: string, semester: number): SemesterPlan | null {
  const roadmap = getRoadmapByBranch(branch);
  return roadmap.find((s) => s.semester === semester) ?? null;
}
